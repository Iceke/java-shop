CREATE TABLE `brand` (
                     `id` int(11) NOT NULL AUTO_INCREMENT,
                     `name` varchar(50) DEFAULT NULL,
                     `image` varchar(50) DEFAULT NULL,
                     `initial` varchar(50) DEFAULT NULL,
                     `sort` int(11) DEFAULT NULL,
                     PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8mb4;

